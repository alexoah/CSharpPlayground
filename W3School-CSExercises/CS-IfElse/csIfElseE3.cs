/**
 *	from C# If...Else: Exercise 3 ( https://www.w3schools.com/cs/exercise.asp?filename=exercise_conditions3 )
 *
 *	question:
 *	Print "Yes" if x is equal to y, otherwise print "No".
 *
 *	int x = 50;
 *	int y = 50;
 *	__ (x __ y) 
 *	{
 *	  Console.WriteLine("Yes");
 *	} 
 *	____
 *	{
 *	  Console.WriteLine("No");
 *	} 
 *
 */
int x = 50;
int y = 50;
if (x == y) 
{
  Console.WriteLine("Yes");
} 
else 
{
  Console.WriteLine("No");
}